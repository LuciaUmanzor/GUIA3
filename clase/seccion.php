<?php
class Seccion {
    private $id;
    private $nombre;
    private $escuela;

    public function getId() {
          return $this->id;
     }

     public function getEscuela() {
        return $this->escuela;

     }

     public function setId($id) {
          $this->id = $id;
     }

     public function setEscuela($escuela) {
          $this->nombre = $nombre;
     }

     public function setEscuela($escuela) {
     $this->escuela = $escuela;

     }

}     


